function score(sub){ return sub.items.reduce((a,it)=>a+(it.v>0?it.v:0),0); }
function clusterScore(sub,name){ return sub.items.filter(it=>it.cluster===name).reduce((a,it)=>a+(it.v>0?it.v:0),0); }
function suppsFor(i,sub){
  const clusters=CLUSTERS[i];
  if(!clusters) return SUPP[i];
  const seen=new Set(); const list=[];
  Object.keys(clusters).forEach(name=>{
    if(clusterScore(sub,name)>=16){
      clusters[name].supps.forEach(s=>{ if(!seen.has(s.name)){ seen.add(s.name); list.push(s); } });
    }
  });
  return list.length ? list : SUPP[i];
}
function bandOf(sub){
  const sum=score(sub);
  if(sub.scale==="bin01"){
    const ratio=sum/sub.items.length;
    if(ratio>=0.4) return {sum,label:"выраженно",focus:true,bg:"var(--bad-bg)",fg:"var(--bad-fg)"};
    if(ratio>=0.25) return {sum,label:"умеренно",focus:true,bg:"var(--warn-bg)",fg:"var(--warn-fg)"};
    if(sum>0) return {sum,label:"низкий",focus:false,bg:"var(--ok-bg)",fg:"var(--ok-fg)"};
    return {sum:0,label:"—",focus:false,bg:"var(--soft)",fg:"var(--muted)"};
  }
  if(sum>=16) return {sum,label:"выраженно",focus:true,bg:"var(--bad-bg)",fg:"var(--bad-fg)"};
  if(sum>=6)  return {sum,label:"умеренно",focus:true,bg:"var(--warn-bg)",fg:"var(--warn-fg)"};
  if(sum>0)   return {sum,label:"низкий",focus:false,bg:"var(--ok-bg)",fg:"var(--ok-fg)"};
  return {sum:0,label:"—",focus:false,bg:"var(--soft)",fg:"var(--muted)"};
}

