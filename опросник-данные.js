function normalizeUnit(u){ return (u||"").toLowerCase().replace(/\s+/g,"").replace("µ","мк"); }
/* сопоставление по границам "слова" (кириллица не покрывается \b в JS regex) —
   чтобы короткие ключи типа "АСТ" не совпадали внутри "гастропанель" или "по возрасту" */
function keyMatchesLine(line, key){
  const l=(line||"").toLowerCase(), k=(key||"").toLowerCase();
  if(!l || !k) return false;
  const isLetter=ch=>/[а-яёa-z0-9]/i.test(ch);
  let idx=l.indexOf(k);
  while(idx!==-1){
    const before=idx>0?l[idx-1]:"";
    const after=idx+k.length<l.length?l[idx+k.length]:"";
    if(!isLetter(before) && !isLetter(after)) return true;
    idx=l.indexOf(k, idx+1);
  }
  return false;
}
/* найти все реальные атомарные анализы (ключи LABRANGES), которые есть в строке рекомендации —
   строка может объединять несколько анализов ("АЛТ, АСТ, ГГТ, ..."), это не один анализ.
   ключи со скобками ("ДГЭА-с (DHEA-S)") проверяем ещё и по короткой части до скобки —
   в тексте рекомендаций иногда встречается только короткое название без английской расшифровки */
function findLabMatches(labLine){
  const rangeMatches=Object.keys(LABRANGES).filter(k=>{
    if(keyMatchesLine(labLine, k)) return true;
    const short=k.split("(")[0].trim();
    if(short && short!==k && keyMatchesLine(labLine, short)) return true;
    const aliases=LABRANGES[k].aliases||[];
    return aliases.some(a=>keyMatchesLine(labLine, a));
  });
  // цикл-зависимые гормоны (Эстрадиол, Прогестерон, ЛГ, ФСГ, Пролактин) не в LABRANGES —
  // у них своя справочная таблица по фазам, но их тоже нужно выделять из составных строк
  const cycleMatches=Object.keys(CYCLE_REFERENCE).filter(k=>keyMatchesLine(labLine, k));
  return [...rangeMatches, ...cycleMatches];
}
function interpretLabValue(key, value, unit, sex, age){
  let num=parseFloat(String(value).replace(",","."));
  if(isNaN(num)) return null;
  if(!LABRANGES[key]) return null;
  const r=LABRANGES[key];
  const uIn=normalizeUnit(unit), uExp=normalizeUnit(r.unit);
  if(uIn && uExp && uIn!==uExp){
    // попробовать распознать альтернативную единицу и сконвертировать
    const altKey = r.altUnits && Object.keys(r.altUnits).find(a=>normalizeUnit(a)===uIn);
    if(altKey){ num = num / r.altUnits[altKey]; }
    else return null; // единица не распознана — не гадаем
  }
  let ref = r.sexSpecific ? (r.ref[sex==="m"?"m":"f"]) : r.ref;
  let opt = r.sexSpecific && r.opt.f ? (r.opt[sex==="m"?"m":"f"]) : r.opt;
  let ageNote="";
  const ageNum=parseInt(age,10);
  if(r.ageBands && !isNaN(ageNum)){
    const band=r.ageBands.find(b=>ageNum<=b.maxAge) || r.ageBands[r.ageBands.length-1];
    if(band){ ref=band.ref; if(band.opt) opt=band.opt; ageNote=" (для возраста "+ageNum+")"; }
  }
  let level, label;
  if(num<ref.low){ level="red"; label="понижено"; }
  else if(num>ref.high){ level="red"; label="повышено"; }
  else if(num<opt.low){ level="yellow"; label="ниже оптимума"; }
  else if(num>opt.high){ level="yellow"; label="выше оптимума"; }
  else { level="green"; label="в оптимуме"; }
  return {level, label, ref, opt, unit:r.unit, source:r.source, sourceUrl:r.sourceUrl, ageNote};
}
